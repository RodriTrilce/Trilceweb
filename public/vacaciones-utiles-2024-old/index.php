      </p>
      <p class="p_terminos">
        <b>1.</b> La presente Política de Protección de Datos Personales Trilce describe el tratamiento que nuestra institución (en adelante Trilce) brinda a los datos personales que recopila de los usuarios en su portal web. Dicha Política se ajusta a las disposiciones contenidas en la Ley de Protección de Datos Personales (Ley N.° 29733), su Reglamento y normas complementarias. Para ello, adoptará las medidas técnicas y organizativas necesarias a fin de evitar la pérdida, mal uso, alteración, acceso no autorizado y robo de los datos personales facilitados por los titulares de datos personales.
      </p>
      <p>
        <strong>II. Definiciones</strong>
      </p>
      <p class="p_terminos">
        <b>2.</b> De acuerdo con la Ley N.° 29733 (Ley de Protección de Datos Personales) y su Reglamento aprobado por el Decreto Supremo N.° 003-2013-JUS, se define lo siguiente:
      </p>
      <p class="p_terminos">
        <strong>a) Datos personales:</strong>
        toda información sobre una persona natural que la identifica o hace identificable a través de medios que pueden ser razonablemente utilizados.
      </p>
      <p class="p_terminos">
        <strong>b) Tratamiento de datos personales:</strong>
        cualquier operación o procedimiento técnico, automatizado o no, que permite la recopilación, registro, organización, almacenamiento, conservación, elaboración, modificación, extracción, consulta, utilización, bloqueo, supresión, comunicación por transferencia o por difusión o cualquier otra forma de procesamiento que facilite el acceso, correlación o interconexión de los datos personales.
      </p>
      <p>
        <strong>III. Consentimiento del Usuario</strong>
      </p>
      <p class="p_terminos">
        <b>3.</b> Trilce se obliga a que de los datos personales obtenidos a través de formularios del portal web, cuenten previamente con el consentimiento expreso del titular para recopilar su información. Estos datos serán tratados única y específicamente para la finalidad solicitada.
      </p>
      <p>
        <strong>IV. Tratamientos de datos personales</strong>
      </p>
      <p class="p_terminos">
        <b>4.</b> Los datos personales facilitados por el Usuario se almacenarán en los bancos de datos que forman parte del sistema de información de Trilce; además, serán tratados para poder llevar a cabo sus finalidades. 
      </p>
      <p class="p_terminos">
        <b>5.</b> Trilce no empleará los datos personales del Usuario para ninguna finalidad distinta a la que fueron suministrados, a no ser que se trate de una finalidad expresamente permitida o exigida por la normativa vigente aplicable, por orden judicial o una autoridad competente.
      </p>
      <p class="p_terminos">
        <b>6.</b> Trilce no compartirá ni cederá a terceros algún dato personal del Usuario sin su consentimiento previo y expreso, salvo cuando dicha comunicación sea exigida por la legislación vigente, por orden judicial o por una autoridad competente. 
      </p>
      <p class="p_terminos">
        <b>7.</b> El portal web de Trilce puede ofrecer enlaces (links) para acceder a otros portales web que no son parte de aquellos. Es responsabilidad del Usuario revisar las políticas de privacidad en dichos portales web para verificar el nivel de protección de sus datos personales en ese ambiente, lo que es ajeno a la responsabilidad de Trilce.
      </p>
      <p>
        <strong>V. Medidas de seguridad</strong>
      </p>
      <p class="p_terminos">
        <b>8.</b> Trilce se compromete a brindar todas las medidas técnicas, legales y administrativas necesarias para garantizar la seguridad y confidencialidad de los datos personales del Usuario. Estos serán tratados considerando los principios de legalidad, consentimiento, proporcionalidad, calidad, seguridad y los demás presentes en la normativa vigente referida a protección de datos personales. Para ello, se evitará que sean filtrados, difundidos o sujetos a cualquier acción que ponga en peligro la información personal del Usuario.
      </p>
      <p>
        <strong>VI. Responsabilidades</strong>
      </p>
      <p class="p_terminos">
        <b>9.</b> Trilce asume que los datos personales proporcionados por el Usuario deben ser verdaderos, completos, exactos, vigentes y corresponder a su verdadera identidad. Ante cualquier modificación, el Usuario será responsable de comunicarlo inmediatamente a Trilce.
      </p>
      <p class="p_terminos">
        <b>10.</b> El Usuario asumirá las responsabilidades por los daños y perjuicios, directos o indirectos, que genere a Trilce y/o terceros por incumplir total o parcialmente lo señalado en el punto anterior.
      </p>
      <p>
        <strong>VII. Ejercicio de los derechos de Acceso, Rectificación, Cancelación y Oposición (derechos ARCO)</strong>
      </p>
      <p class="p_terminos">
        <b>11.</b> Trilce debe garantizar la atención de los derechos protegidos que pueda ejercer el Titular de los Datos Personales. Para ello, debe mantener disponibles canales, procedimientos e información a fin de atender la solicitud del Usuario en los plazos establecidos por las normas de protección de datos personales.
      </p>
      <p class="p_terminos">
        <b>12.</b> El Usuario podrá ejercitar sus derechos de Información, Acceso, Rectificación, Cancelación, Revocación, Oposición, etc. (derechos ARCO) al uso de sus datos personales, de conformidad con la Ley de Protección de Datos Personales (Ley N.° 29733). En ese sentido, el Usuario tiene derecho, entre otros, a acceder a su información personal, a solicitar la rectificación de datos inexactos y a revocar su consentimiento para su tratamiento. Asimismo, podrá solicitar la supresión de sus datos u oponerse a su tratamiento, incluso cuando estos ya no resulten necesarios para los fines que motivaron su recopilación.
      </p>
      <p class="p_terminos">
        <b>13.</b> El ejercicio de estos derechos es gratuito. Para ello, el Usuario puede dirigirse por correo electrónico a datospersonales@trilce.edu.pe, adjuntando una solicitud y copia de su documento de identidad (DNI/CE/Pasaporte) que acredite su titularidad sobre los datos personales respecto de los cuales ejercerá su derecho. También podrá hacerlo en cualquiera de nuestras oficinas ubicadas en el país. Si utiliza un representante legal, este deberá acreditarse como tal.
